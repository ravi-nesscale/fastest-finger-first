<template>
  <div class="min-h-screen bg-gradient-to-br from-[#0f2027] via-[#203a43] to-[#2c5364] text-white py-10 px-4 font-['Poppins']">
    <h1 class="text-5xl font-extrabold text-center text-yellow-300 mb-14 animate-slide-in glow-text tracking-wider">
      👑 KBC Leaderboard
    </h1>

    <!-- Top 3 Podium -->
    <div class="flex flex-wrap justify-center items-end gap-10 mb-16 animate-slide-in">
      <!-- 2nd Place -->
      <div class="flex flex-col items-center space-y-2">
        <img src="/media/silver.png" class="w-20 h-20 rounded-full bg-white border-4 border-slate-300 shadow-md" />
        <div class="bg-white/10 px-4 py-1 rounded-full text-sm font-semibold">🥈 {{ topThree[1]?.player_name || '---' }}</div>
        <div class="text-blue-300 text-xs font-semibold">🏅 {{ topThree[1]?.total_points || 0 }}</div>
        <div class="bg-gray-600 w-28 h-28 flex items-center justify-center text-xl font-bold rounded-t-xl podium-glass">2</div>
      </div>

      <!-- 1st Place -->
      <div class="flex flex-col items-center space-y-3 relative">
        <div class="absolute -top-10 text-4xl animate-bounce">👑</div>
        <img src="/media/gold.jpg" class="w-24 h-24 rounded-full border-4 border-yellow-400 shadow-2xl animate-pulse" />
        <div class="bg-yellow-300 text-black px-4 py-1 rounded-full font-bold text-sm">🥇 {{ topThree[0]?.player_name || '---' }}</div>
        <div class="text-yellow-100 text-sm font-bold">🏅 {{ topThree[0]?.total_points || 0 }}</div>
        <div class="bg-yellow-500 w-32 h-36 flex items-center justify-center text-3xl font-extrabold rounded-t-xl podium-glass">1</div>
      </div>

      <!-- 3rd Place -->
      <div class="flex flex-col items-center space-y-2">
        <img src="/media/bronze.jpg" class="w-20 h-20 rounded-full border-4 border-orange-400 shadow-md" />
        <div class="bg-orange-400 text-black px-3 py-1 rounded-full text-sm font-semibold">🥉 {{ topThree[2]?.player_name || '---' }}</div>
        <div class="text-orange-200 text-sm font-semibold">🏅 {{ topThree[2]?.total_points || 0 }}</div>
        <div class="bg-gray-700 w-24 h-24 flex items-center justify-center text-xl font-bold rounded-t-xl podium-glass">3</div>
      </div>
    </div>

    <!-- Other Players -->
    <div class="max-w-2xl mx-auto space-y-4">
      <div
        v-for="(player, index) in leaderboard.slice(3)"
        :key="player.player_id"
        class="flex items-center justify-between backdrop-blur-md bg-white/10 hover:bg-white/20 transition-all duration-200 px-4 py-3 rounded-xl shadow-lg"
      >
        <div class="flex items-center gap-4">
          <span class="text-lg font-bold text-gray-300">{{ index + 4 }}</span>
          <span class="font-medium">{{ player.player_name }}</span>
        </div>
        <div class="text-green-300 font-semibold">🏅 {{ player.total_points }}</div>
      </div>
    </div>
  </div>
</template>

<script setup>
import { ref, onMounted, computed } from 'vue'
import axios from 'axios'

const leaderboard = ref([])

const fetchLeaderboard = async () => {
  try {
    const res = await axios.get('/api/method/kbc.api.leaderboard.get_leaderboard')
    leaderboard.value = res.data.message || []
  } catch (err) {
    console.error('Error fetching leaderboard:', err)
  }
}

onMounted(() => {
  fetchLeaderboard()
})

const topThree = computed(() => leaderboard.value.slice(0, 3))
</script>

